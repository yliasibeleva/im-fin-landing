"""
Генератор стандартных дедлайнов по российскому налоговому календарю.
Учитывает систему налогообложения, наличие сотрудников, воинский учёт.
"""
from datetime import date, timedelta
from typing import List, Dict


def next_workday(d: date) -> date:
    """Если дата выпадает на выходной — переносим на ближайший понедельник."""
    while d.weekday() >= 5:
        d += timedelta(days=1)
    return d


def _d(year: int, month: int, day: int) -> str:
    """Создаём рабочую дату и возвращаем ISO-строку."""
    return next_workday(date(year, month, day)).isoformat()


def generate_deadlines(
    company_id: int,
    tax_system: str,      # ОСН / УСН / ЕСХН / ПСН
    org_type: str,        # ООО / ИП
    has_employees: bool,
    has_military: bool,
    year: int = None,
) -> List[Dict]:
    """
    Возвращает список словарей для bulk-вставки в report_deadlines.
    Генерирует дедлайны на указанный год (по умолчанию — текущий).
    Дедлайны со сроком в следующем году тоже добавляются (годовые отчёты).
    """
    if year is None:
        year = date.today().year

    rows: List[Dict] = []
    y = year       # текущий год
    y1 = year + 1  # следующий год (для годовых отчётов)

    def add(name: str, rtype: str, due_date: str, period: str):
        rows.append({
            'company_id':  company_id,
            'report_name': name,
            'report_type': rtype,
            'due_date':    due_date,
            'period':      period,
        })

    # ── Бухгалтерская отчётность (все кроме ИП) ──────────────────────────────
    if org_type == 'ООО':
        add('Бухгалтерская отчётность', 'БО', _d(y1, 3, 31), f'{y} год')

    # ── ОСН ──────────────────────────────────────────────────────────────────
    if tax_system == 'ОСН':
        # НДС — декларации (25-го числа месяца, следующего за кварталом)
        add('Декларация НДС, I кв.', 'НДС', _d(y, 4, 25), f'I кв. {y}')
        add('Декларация НДС, II кв.', 'НДС', _d(y, 7, 25), f'II кв. {y}')
        add('Декларация НДС, III кв.', 'НДС', _d(y, 10, 25), f'III кв. {y}')
        add('Декларация НДС, IV кв.', 'НДС', _d(y1, 1, 25), f'IV кв. {y}')

        # НДС — платежи (по 1/3 до 28-го в каждом из трёх месяцев квартала)
        nds_payments = [
            (1, 28, f'IV кв. {y-1} — 1/3'), (2, 28, f'IV кв. {y-1} — 2/3'),
            (3, 28, f'IV кв. {y-1} — 3/3'),
            (4, 28, f'I кв. {y} — 1/3'),   (5, 28, f'I кв. {y} — 2/3'),
            (6, 28, f'I кв. {y} — 3/3'),
            (7, 28, f'II кв. {y} — 1/3'),  (8, 28, f'II кв. {y} — 2/3'),
            (9, 28, f'II кв. {y} — 3/3'),
            (10, 28, f'III кв. {y} — 1/3'), (11, 28, f'III кв. {y} — 2/3'),
            (12, 28, f'III кв. {y} — 3/3'),
        ]
        for month, day, period in nds_payments:
            add(f'Платёж НДС ({period})', 'Платёж НДС', _d(y, month, day), period)

        # Налог на прибыль — только для ООО (ИП платит НДФЛ, не налог на прибыль)
        if org_type == 'ООО':
            add('Декларация прибыль, I кв.', 'Прибыль', _d(y, 4, 25), f'I кв. {y}')
            add('Декларация прибыль, пол.', 'Прибыль', _d(y, 7, 25), f'Пол. {y}')
            add('Декларация прибыль, 9 мес.', 'Прибыль', _d(y, 10, 25), f'9 мес. {y}')
            add('Декларация прибыль, год', 'Прибыль', _d(y1, 3, 25), f'{y} год')
            add('Аванс налог на прибыль, I кв.', 'Платёж Прибыль', _d(y, 4, 28), f'I кв. {y}')
            add('Аванс налог на прибыль, пол.', 'Платёж Прибыль', _d(y, 7, 28), f'Пол. {y}')
            add('Аванс налог на прибыль, 9 мес.', 'Платёж Прибыль', _d(y, 10, 28), f'9 мес. {y}')
            add('Налог на прибыль, год', 'Платёж Прибыль', _d(y1, 3, 28), f'{y} год')
            # Налог на имущество — декларация и платежи только для ООО
            add('Декларация налог на имущество', 'Имущество', _d(y1, 2, 25), f'{y} год')
            add('Платёж налог на имущество, год', 'Платёж Имущество', _d(y1, 2, 28), f'{y} год')
            add('Аванс налог на имущество, I кв.', 'Платёж Имущество', _d(y, 4, 28), f'I кв. {y}')
            add('Аванс налог на имущество, пол.', 'Платёж Имущество', _d(y, 7, 28), f'Пол. {y}')
            add('Аванс налог на имущество, 9 мес.', 'Платёж Имущество', _d(y, 10, 28), f'9 мес. {y}')

        # 3-НДФЛ, авансы и итоговый платёж — только для ИП на ОСН
        if org_type == 'ИП':
            add('Декларация 3-НДФЛ', 'Прибыль', _d(y1, 4, 30), f'{y} год')
            add('Аванс НДФЛ ИП, I кв.', 'Платёж НДФЛ', _d(y, 4, 25), f'I кв. {y}')
            add('Аванс НДФЛ ИП, пол.', 'Платёж НДФЛ', _d(y, 7, 25), f'Пол. {y}')
            add('Аванс НДФЛ ИП, 9 мес.', 'Платёж НДФЛ', _d(y, 10, 25), f'9 мес. {y}')
            # Итоговый платёж НДФЛ за год — до 15 июля следующего года (ст. 227 НК РФ п. 6)
            add('НДФЛ ИП итоговый платёж, год', 'Платёж НДФЛ', _d(y1, 7, 15), f'{y} год')

    # ── УСН ──────────────────────────────────────────────────────────────────
    if tax_system == 'УСН':
        # С 2024: декларация — 25-го, платёж — 28-го (разные даты!)
        decl_month = 3 if org_type == 'ООО' else 4   # март (ООО) / апрель (ИП)
        add('Декларация УСН', 'УСН', _d(y1, decl_month, 25), f'{y} год')
        add('Налог УСН, год', 'Платёж УСН', _d(y1, decl_month, 28), f'{y} год')

        # Авансовые платежи УСН — 28-го месяца, следующего за кварталом
        add('Аванс УСН, I кв.', 'Платёж УСН', _d(y, 4, 28), f'I кв. {y}')
        add('Аванс УСН, пол.', 'Платёж УСН', _d(y, 7, 28), f'Пол. {y}')
        add('Аванс УСН, 9 мес.', 'Платёж УСН', _d(y, 10, 28), f'9 мес. {y}')

    # ── ЕСХН ─────────────────────────────────────────────────────────────────
    if tax_system == 'ЕСХН':
        add('Декларация ЕСХН', 'ЕСХН', _d(y1, 3, 31), f'{y} год')
        add('Аванс ЕСХН, пол.', 'Платёж ЕСХН', _d(y, 7, 28), f'Пол. {y}')
        add('Налог ЕСХН, год', 'Платёж ЕСХН', _d(y1, 3, 31), f'{y} год')

    # ── ПСН ──────────────────────────────────────────────────────────────────
    # При ПСН нет деклараций, только оплата патента (вносится вручную)

    # ── Сотрудники (для любой системы) ───────────────────────────────────────
    if has_employees:
        quarters = [
            ('I кв.', 4, 25, f'I кв. {y}'),
            ('II кв.', 7, 25, f'II кв. {y}'),
            ('III кв.', 10, 25, f'III кв. {y}'),
            ('IV кв.', 1, 25, f'IV кв. {y}'),   # следующий год
        ]

        for q_name, month, day, period in quarters:
            yy = y if month != 1 else y1
            due = _d(yy, month, day)
            add(f'6-НДФЛ, {q_name}', '6-НДФЛ', due, period)
            add(f'РСВ, {q_name}', 'РСВ', due, period)
            add(f'ЕФС-1 (подр. 1.1), {q_name}', 'ЕФС-1', due, period)
            add(f'ЕФС-1 (НС и ПЗ), {q_name}', 'ЕФС-1', due, period)

        # 6-НДФЛ — годовой (25 февраля следующего года)
        add('6-НДФЛ, год', '6-НДФЛ', _d(y1, 2, 25), f'{y} год')

        # ЕФС-1 Подраздел 1.2 (годовой стаж) — 1 марта следующего года
        add('ЕФС-1 (подр. 1.2, стаж), год', 'ЕФС-1', _d(y1, 3, 1), f'{y} год')

        months_ru = ['Янв', 'Фев', 'Мар', 'Апр', 'Май', 'Июн',
                     'Июл', 'Авг', 'Сен', 'Окт', 'Ноя', 'Дек']

        # Персонифицированные сведения о физлицах — 25-го каждого месяца (с 2023, замена СЗВ-М)
        for m in range(1, 13):
            add(
                f'Перс. сведения о физлицах, {months_ru[m-1]} {y}',
                'ЕФС-1',
                _d(y, m, 25),
                f'{months_ru[m-1]} {y}'
            )

        # Страховые взносы — 28-го каждого месяца
        for m in range(1, 13):
            add(
                f'Страховые взносы, {months_ru[m-1]} {y}',
                'Платёж СВ',
                _d(y, m, 28),
                f'{months_ru[m-1]} {y}'
            )

        # НДФЛ — два срока в месяц (с 2024 года)
        # 28-е: за период 1-22 числа текущего месяца
        # 5-е следующего: за период 23-последнее число текущего
        for m in range(1, 13):
            add(
                f'НДФЛ (1–22), {months_ru[m-1]} {y}',
                'Платёж НДФЛ',
                _d(y, m, 28),
                f'{months_ru[m-1]} {y}'
            )
            nm = m % 12 + 1
            ny = y if m < 12 else y1
            add(
                f'НДФЛ (23–{months_ru[m-1]}), {months_ru[m-1]} {y}',
                'Платёж НДФЛ',
                _d(ny, nm, 5),
                f'{months_ru[m-1]} {y}'
            )

    # ── Воинский учёт ────────────────────────────────────────────────────────
    if has_military:
        add('Список военнообязанных (обновление)', 'Воинский учёт', _d(y, 4, 1), f'{y} год')
        add('Форма № 6 (численность)', 'Воинский учёт', _d(y, 11, 1), f'{y} год')
        add('Сведения о гражданах, подлежащих ВУ', 'Воинский учёт', _d(y, 11, 10), f'{y} год')

    # ── Страховые взносы ИП за себя (для любой СНО) ──────────────────────────
    if org_type == 'ИП':
        # Фиксированная часть — до 31 декабря текущего года
        add('СВ ИП за себя (фиксированная часть)', 'Платёж СВ', _d(y, 12, 31), f'{y} год')
        # 1% с дохода свыше 300 000 ₽ — до 1 июля следующего года
        add('СВ ИП за себя (1% с дохода > 300 тыс.)', 'Платёж СВ', _d(y1, 7, 1), f'{y} год')

    return rows


